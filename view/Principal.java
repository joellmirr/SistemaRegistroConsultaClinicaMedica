    private void jButton_BuscarPaciente_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarPaciente_ActionPerformed
        
          //Para receber objetodentro de um metodo,esta variavel deve ser declarada como variavel global
         
         
         String CPF = JOptionPane.showInputDialog("Informe o CPF");
        String sqlId = "SELECT idpaciente FROM Paciente WHERE cpf = ?";
        
        try {
            PreparedStatement ps = conn.getConexao().prepareStatement(sqlId);
            ps.setString(1, CPF);
            ResultSet rs = ps.executeQuery();
            
            if (rs.next()) {
                Buscar_P = rs.getInt("idpaciente");
                JOptionPane.showMessageDialog(null, "Busca Confirmada ");
            }else{
                JOptionPane.showMessageDialog(null, "Paciente Não Cadastrado! ");
            }
            
            ps.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro SQL: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton_BuscarPaciente_ActionPerformed

