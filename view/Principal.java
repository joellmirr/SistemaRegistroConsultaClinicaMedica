    public void Limpar(){
        jTextField_dataConsulta.setText("");
        jTextField_ValorConsulta.setText("");
        jTextField_motivoConsulta.setText("");
        jTextField_nomeExame.setText("");
        jTextField_DescricaoExame.setText("");
    }
    
    public ConsultaM MontarExame(){
        ConsultaM e = new ConsultaM();
        e.setPaciente(Buscar_P);
        e.setDataconsulta(jTextField_dataConsulta.getText());
        e.setValorconsulta(Float.parseFloat(jTextField_ValorConsulta.getText()));
        e.setMotivoconsulta(jTextField_motivoConsulta.getText());
        e.setNomeexame(jTextField_nomeExame.getText());
        e.setDescricaoexame(jTextField_DescricaoExame.getText());
        e.setMedico(Buscar_M);
        return e;
    }
    
    
